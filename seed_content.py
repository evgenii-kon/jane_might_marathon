def run():
    conn = psycopg2.connect(**DB_CONFIG)
    cur = conn.cursor()

    print("🔗 Подключение к БД установлено.")

    # ── 1. Обновить описания недель ──────────────────────────
    print("\n📅 Обновляю описания недель...")
