async def main():
    async with AsyncSessionLocal() as db:
        result = await db.execute(
            select(Lesson, Week.number)
            .join(Week, Lesson.week_id == Week.id)
            .order_by(Week.number, Lesson.order_in_week)
        )
        rows = result.all()

        print(f"Найдено уроков в БД: {len(rows)}, дней в сценарии: {len(DAYS)}\n")
        for i, (lesson, week_number) in enumerate(rows, start=1):
            marker = "<-- будет залито" if i <= len(DAYS) else ""
            print(f"{i:>3} | lesson_id={lesson.id:<4} week={week_number} "
                  f"order_in_week={lesson.order_in_week:<3} name={lesson.name} {marker}")

        if len(rows) < len(DAYS):
            print(f"\nВНИМАНИЕ: в сценарии {len(DAYS)} дней, а уроков в БД только {len(rows)}. "
                  f"Лишние дни сценария будут пропущены.")

        total_lines = 0
        for day_index, day_lines in enumerate(DAYS):
            if day_index >= len(rows):
                break
            lesson, _week_number = rows[day_index]
            lesson_id = lesson.id

            await db.execute(delete(NovelLine).where(NovelLine.lesson_id == lesson_id))

            for order, line in enumerate(day_lines):
                db.add(NovelLine(
                    lesson_id=lesson_id,
                    order=order,
                    type=line["type"],
                    character=line["character"],
                    speaker=line["speaker"],
                    text=line["text"],
                    side=line["side"],
                ))
                total_lines += 1

            await db.commit()
            print(f"День {day_index + 1} -> lesson_id={lesson_id} ({lesson.name}): "
                  f"{len(day_lines)} реплик залито")

        print(f"\nГотово. Всего залито реплик: {total_lines}")


if __name__ == "__main__":
    asyncio.run(main())
