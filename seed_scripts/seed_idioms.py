def seed_idioms(cur):
    print("\n成语 Вставляю чэнъюи...")
    for idm in IDIOMS:
        cur.execute(
            """INSERT INTO idioms
               (hanzi, pinyin, translate, meaning, story, example, example_translation, audio_url)
               VALUES (%s, %s, %s, %s, %s, %s, %s, %s)""",
            (
                idm["hanzi"],
                idm["pinyin"],
                idm["translate"],
                idm["meaning"],
                idm["story"],
                idm["example"],
                idm["example_translation"],
                idm["audio_url"],
            ),
        )
        print(f"   + {idm['hanzi']} ({idm['pinyin']})")
    print(f"   ✅ Вставлено идиом: {len(IDIOMS)}")

