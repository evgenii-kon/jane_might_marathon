def seed_weeks(cur):
    print("\n📅 Вставляю недели...")
    week_ids = {}
    for w in WEEKS:
        cur.execute(
            """INSERT INTO weeks
               (slug, short_description, long_description, number,
                target_words_count, target_exercises_count)
               VALUES (%s, %s, %s, %s, %s, %s)
               RETURNING id""",
            (
                w["slug"],
                w["short_description"],
                w["long_description"],
                w["number"],
                w["target_words_count"],
                w["target_exercises_count"],
            ),
        )
        week_id = cur.fetchone()[0]
        week_ids[w["number"]] = week_id
        print(f"   + Неделя {w['number']}: {w['short_description']} (id={week_id})")
    return week_ids

