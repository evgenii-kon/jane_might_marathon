def h2(text: str) -> str:
    return f"<h2>{text}</h2>"


def p(text: str) -> str:
    return f"<p>{text}</p>"


def ul(items: list) -> str:
    lis = "\n".join(f"  <li>{item}</li>" for item in items)
    return f"<ul>\n{lis}\n</ul>"


def table(headers: list, rows: list) -> str:
    ths = "".join(f"<th>{h}</th>" for h in headers)
    trs = "\n".join(
        "  <tr>" + "".join(f"<td>{cell}</td>" for cell in row) + "</tr>"
        for row in rows
    )
    return f"<table>\n  <tr>{ths}</tr>\n{trs}\n</table>"


def vocab_table(entries: list) -> str:
    """entries: список (иероглиф, пиньинь, перевод)."""
    rows = [
        (f'<span class="audio-hanzi" data-audio="{hanzi}">{hanzi}</span>', pinyin, ru)
        for hanzi, pinyin, ru in entries
    ]
    return table(["Иероглиф", "Пиньинь", "Перевод"], rows)


# Иероглифы + типичная китайская пунктуация внутри предложения (для примеров
# вида "你好吗？你呢？", которые должны звучать одной фразой, а не рассыпаться
# на "你好吗" и "你呢").
_HANZI_RUN_RE = re.compile(r"[一-鿿。，！？：；]+")
_TD_CELL_RE = re.compile(r"(<td>)(.*?)(</td>)", re.DOTALL)


def _wrap_hanzi_runs(cell: str) -> str:
    if "data-audio=" in cell:
        return cell  # уже обёрнуто (vocab_table) — не дублируем span
    return _HANZI_RUN_RE.sub(
        lambda m: f'<span class="audio-hanzi" data-audio="{m.group(0)}">{m.group(0)}</span>',
        cell,
    )


def linkify_table_hanzi(html: str) -> str:
    """Оборачивает иероглифы в ячейках обычных table() (согласные, гласные,
    тоны, ключи, счётные слова, вопросительные слова и т.п.) в
    <span data-audio="..."> — так же, как vocab_table() делает для «Новых
    слов». Применяется один раз ко всему готовому content_html урока (после
    build()), поэтому уже проставленный vocab_table()'ом data-audio не
    задевает — см. проверку в _wrap_hanzi_runs. Ячеек вне <td> (диалоги,
    списки, абзацы) эта функция не касается."""
    return _TD_CELL_RE.sub(
        lambda m: m.group(1) + _wrap_hanzi_runs(m.group(2)) + m.group(3), html
    )


# Персонажи диалогов: слаг (для data-character/CSS-цвета аватара) и буква(-ы)
# в кружке-аватаре. Пользователь — всегда "user", его аватар и подпись — JS
# в lesson_detail.html подставляет вместо {{user_name}} / {{user_initial}}.
CHARACTER_META = {
    "Жулан": {"slug": "zhulan", "initial": "Ж"},
    "Конфуси": {"slug": "konfusi", "initial": "К"},
    "Чингису": {"slug": "chingisu", "initial": "Ч"},
    "Брис Лэй": {"slug": "bris_lei", "initial": "Б"},
    "Жо": {"slug": "zho", "initial": "Жо"},
}


def dialogue(title: str, lines: list) -> str:
    """lines: список (speaker, hanzi, pinyin, ru).

    speaker == "{{user_name}}" помечает реплику пользователя
    (data-character="user"), остальные — персонажи из CHARACTER_META.

    data-audio на .dl-line — плейсхолдер (иероглифы), который заменяет на
    ссылку S3 audio_generator.py. Кнопки .dl-play-btn (реплика) и
    .dl-play-all-btn (весь диалог по порядку) ничего не проигрывают сами —
    по клику их обрабатывает JS в lesson_detail.html. Стили —
    app/static/styles/main_new.css, секция "LESSON DIALOGUE".
    """
    rows = [
        '  <div class="dl-header">',
        f'    <div class="dl-header-title">💬 Диалог — {title}</div>',
        '    <button type="button" class="dl-play-all-btn">▶ Слушать всё</button>',
        "  </div>",
    ]
    for speaker, hanzi, pinyin, ru in lines:
        if speaker == "{{user_name}}":
            character = "user"
            initial = "{{user_initial}}"
        else:
            meta = CHARACTER_META.get(speaker, {"slug": "npc", "initial": speaker[:1]})
            character = meta["slug"]
            initial = meta["initial"]
        rows.append(
            f'  <div class="dl-line" data-speaker="{speaker}" data-character="{character}" data-audio="{hanzi}">\n'
            f'    <div class="dl-avatar">{initial}</div>\n'
            f'    <div class="dl-body">\n'
            f'      <div class="dl-speaker">{speaker}</div>\n'
            f'      <div class="dl-hanzi-row">\n'
            f'        <span class="dl-hanzi">{hanzi}</span>\n'
            f'        <button type="button" class="dl-play-btn" aria-label="Слушать">▶</button>\n'
            f'      </div>\n'
            f'      <div class="dl-pinyin">{pinyin}</div>\n'
            f'      <div class="dl-ru">{ru}</div>\n'
            f'    </div>\n'
            f"  </div>"
        )
    body = "\n".join(rows)
    return f'<div class="lesson-dialogue">\n{body}\n</div>'


def lesson_week_and_order(day: int) -> tuple:
    """1-7 -> неделя 1, 8-14 -> неделя 2, 15-21 -> неделя 3, 22-28 -> неделя 4."""
    week_number = (day - 1) // 7 + 1
    order_in_week = (day - 1) % 7 + 1
    return week_number, order_in_week


