def homework(items: list) -> str:
    """Блок «Домашнее задание» в конце теории урока, отделённый <hr>."""
    return build(
        "<hr>",
        h2("Домашнее задание"),
        ul(items),
    )


def build(*blocks: str) -> str:
    return "\n".join(blocks)


