def seed_lessons_v2(cur, lessons_data=None):
    """Обновляет content_html уроков (28 дней) из lessons_data (по умолчанию —
    LESSONS_DATA_V2 из этого модуля).

    lessons_data можно передать явно — например, список с уже подставленными
    аудио-ссылками из файла, который получается после прогона audio_generator.py
    (см. render_helper_based_module() в audio_generator.py: он импортирует этот
    модуль, разворачивает LESSONS_DATA_V2 в литеральные строки и заменяет в них
    data-audio="иероглифы" на data-audio="https://...", — итоговый файл тоже
    определяет LESSONS_DATA_V2 и может быть импортирован и передан сюда).

    Работает как UPSERT по уникальному полю lessons.name: если урок с таким
    именем уже существует (создан старым seed_scripts/seed_lessons.py) — его
    content_html/video_url/week_id/order_in_week обновляются на месте, id
    урока не меняется (важно для FK из exercises, word_lesson_association,
    user_lesson_progress). Если урока с таким именем ещё нет — он создаётся.
    """
    if lessons_data is None:
        lessons_data = LESSONS_DATA_V2

    print("\n📚 Обновляю content_html уроков (v2)...")
    cur.execute("SELECT number, id FROM weeks ORDER BY number")
    week_ids = dict(cur.fetchall())
    if not week_ids:
        raise RuntimeError(
            "Таблица weeks пуста. Сначала выполните seed_scripts/seed_weeks.py "
            "(или seed_scripts/seed_main.py), чтобы недели существовали."
        )

    lesson_ids = {}
    for day, name, content_html in lessons_data:
        week_number, order_in_week = lesson_week_and_order(day)
        week_id = week_ids[week_number]
        cur.execute(
            """INSERT INTO lessons (name, week_id, order_in_week, content_html, video_url)
               VALUES (%s, %s, %s, %s, %s)
               ON CONFLICT (name) DO UPDATE SET
                   week_id = EXCLUDED.week_id,
                   order_in_week = EXCLUDED.order_in_week,
                   content_html = EXCLUDED.content_html,
                   video_url = EXCLUDED.video_url
               RETURNING id""",
            (name, week_id, order_in_week, content_html, None),
        )
        lesson_id = cur.fetchone()[0]
        lesson_ids[name] = lesson_id
        print(f"   ↻ «{name}» (день {day}, week={week_number}, order={order_in_week}, id={lesson_id})")
    print(f"   ✅ Обновлено/вставлено уроков: {len(lessons_data)}")
    return lesson_ids


def run(module_path: str = None):
    """module_path — необязательный путь к альтернативному .py-файлу,
    определяющему LESSONS_DATA_V2 (например, файл с уже подставленными
    аудио-ссылками, полученный через audio_generator.py). Если не указан —
    используются данные из этого модуля (без ссылок на аудио в data-audio)."""
    from seed_scripts.db import get_connection

    lessons_data = LESSONS_DATA_V2
    if module_path:
        import importlib.util

        spec = importlib.util.spec_from_file_location("_seed_lessons_v2_external", module_path)
        external = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(external)
        lessons_data = external.LESSONS_DATA_V2
        print(f"📄 Использую LESSONS_DATA_V2 из {module_path} ({len(lessons_data)} уроков)")

    conn = get_connection()
    cur = conn.cursor()
    try:
        seed_lessons_v2(cur, lessons_data=lessons_data)
        conn.commit()
        print("\n✅ Готово! content_html уроков обновлён (v2).")
    except Exception:
        conn.rollback()
        raise
    finally:
        cur.close()
        conn.close()

    invalidate_lessons_cache()


def invalidate_lessons_cache():
    """LessonService (app/services/lesson_service.py) кэширует уроки в Redis
    на 10 минут (ключи lessons:id:<id> и lessons:all) и НЕ узнаёт о прямых
    правках в Postgres мимо своего слоя — без этого сайт отдавал бы старый
    content_html вплоть до истечения TTL. Best-effort: если Redis недоступен,
    просто предупреждаем, не роняя сид (кэш всё равно протухнет сам по TTL)."""
    import os

    try:
        import redis as redis_sync
    except ImportError:
        print("⚠️  Пакет redis не установлен — пропускаю инвалидацию кэша уроков "
              "(старый контент будет отдаваться до истечения TTL, обычно 10 минут).")
        return

    host = os.getenv("REDIS_HOST", "localhost")
    port = int(os.getenv("REDIS_PORT", 6379))
    db = int(os.getenv("REDIS_DB", 0))
    password = os.getenv("REDIS_PASSWORD") or None

    try:
        client = redis_sync.Redis(host=host, port=port, db=db, password=password)
        keys = client.keys("lessons:*")
        if keys:
            client.delete(*keys)
        print(f"🧹 Инвалидировал кэш уроков в Redis ({len(keys)} ключ(ей) lessons:*).")
    except Exception as exc:
        print(f"⚠️  Не удалось инвалидировать кэш уроков в Redis ({exc}) — "
              "старый контент может отдаваться до истечения TTL.")


